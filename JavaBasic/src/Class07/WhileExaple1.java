package Class07;

public class WhileExaple1 {

	public static void main(String[] args) {
		
		int a=1;
		
		while (a<=100) {
			System.out.print(a+" ");
			a++;
		}
		System.out.println("---------");
		
		int b=20;
		while (b<=100) {
			System.out.print(b +" ");
			b+=2;
		}
		System.out.println("--------");
		int c=1;
		while(c<=100) {
			System.out.print(c + " ");
		
		}
		}

}
